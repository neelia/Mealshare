				<span class="info-icon general-icon"></span>
				<h2>Was ist GLFtv?</h2>
				<p>GLFtv ist Hochschulfernsehen von Studierenden der Hochschule Furtwangen University. In Berichten, Reportagen, Interviews oder Kurzfilmen erzählen sie von den Themen, die sie bewegen, mal informativ, mal humorvoll und mit viel Engagement. 
					GLFtv streamt während des Semesters wöchentliche Live-Sendungen auf www.GLFtv.de. Vorproduzierte Magazinsendungen laufen im baden-württembergischen Kabelfernsehen bei <a href="http://www.hd-campus.de/" class="text-link">HD Campus </a>und <a href="http://www.tv-suedbaden.de/sendungen/glftv/" class="text-link">TV Südbaden</a>.
				</p>
			</div>
			<div class="info-content">
				<span class="info-icon idea-icon"></span>
				<h2>Die Idee dahinter</h2>
				<p>Medientechnik, Mediengestaltung, Medienkonzeption, journalistische Redaktionsarbeit in der TV-Redaktion und online, crossmediales Denken, Interviewführung, Präsentation und Moderation: GLFtv bietet den Studierenden ein realitätsnahes Umfeld, in dem sie ihre Fähigkeiten für ein echtes Publikum praktisch anwenden können. Viele bei GLFtv erworbene Fähigkeiten werden dabei auch außerhalb einer TV-Redaktion von künftigen Arbeitgebern gefordert.
				</p>
			</div>
			<div class="info-content">
				<span class="info-icon goal-icon"></span>
				<h2>Unser Ziel</h2>
				<p>GLFtv möchte die journalistischen, konzeptionellen und technischen Fähigkeiten der Studierenden ausbilden. Darüber hinaus sollen Softskills wie Teamfähigkeit, Verantwortung, Persönlichkeitsentwicklung und die Fähigkeit des sicheren Auftretens und Präsentierens gefestigt werden. Die produzierten Filme und Sendungen sollen eine Qualität erreichen, die die Zuschauer zufrieden stellt. Gleichzeitig lässt die Lernumgebung den Studierenden Raum, neue Ideen zu entwickeln und auszuprobieren. 
				</p>
