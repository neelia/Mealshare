				<h2>Chronik</h2>
			</div>
			<div class="chronik-line"></div>
			<div class="chronik-content chronik-content-head">
				<p class="text">Nachdem sich die private Radioinitiative ‘Radio Diarrhoe’ zweier Medieninformatik- Studenten einige Male im Tonstudio des Fachbereichs ein gutes Mikrofon ausgeliehen hatte, kam dort die Idee auf, allen radiobegeisterten Studenten eine ‘offizielle’ Plattform zu bieten, sich in dieser Richtung zu betätigen.</p>
				<div class="start-bubble"></div>
			</div>
			<div class="chronik-content chronik-content-left">
				<div class="bubble">
					<p class="number">1999</p>
				</div>
				<p class="text">Erste Versuche des WebRadios GLFtv am Fachbereich Digitale Medien der FH Furtwangen im Sommersemester. In einem Wahlfach bei Prof. Schäfer- Schönthal entstehen im Tonstudio einige Live-Sendungen, Macher natürlich MI-Studenten, Zuhörer zunächst FH-Mitglieder. Inhaltlich und formal kann frei experimentiert werden. Der Versuchscharakter und der Spaß am Radiomachen stehen im Vordergrund. Während den Sendungen setzt per eMail und ICQ ein reger Austausch zwischen Zuhörern und Redaktion ein, der den interaktiven Charakter dieses neuen Mediums unterstreicht.</p>
			</div>
			<div class="chronik-content chronik-content-right">
				<div class="bubble">
					<p class="number">1999/ 2000</p>
				</div>
				<p class="text">liegt der Schwerpunkt auf dem Ausbau der interaktiven und audiovisuellen Möglichkeiten von Radio und TV per Internet sowie auf einer stärker inhaltlich ausgerichteten journalistischen Ausbildung. Diese wird idealerweise unterstützt durch das in Furtwangen parallel laufende Seminar ‘Journalistische Beitragsproduktion für den Hörfunk’ bei Prof. Dr. Martens (FH Kehl), der seinerseits im Sommersemester schon ein Radioprojekt in Kehl initiiert hatte und dieses zu einem regelmäßigen ‘Office Radio’ ausbaut.</p>
			</div>
			<div class="chronik-content chronik-content-left">
				<div class="bubble">
					<p class="number">2000</p>
				</div>
				<p class="text">Im Sommersemester folgt der Schritt zum WebTV aus dem Videostudio mit 3 Kameras und Bildmischer. Ebenfalls gelingt es, auf einer ‘Dia-Leinwand’ live Bilder zu den Beiträgen zu ‘senden’ (pushen), die direkt im Browser erscheinen.</p>
			</div>
			<div class="chronik-content chronik-content-right">
				<div class="bubble">
					<p class="number">2001</p>
				</div>
				<p class="text">Verbesserte Teilnahme aus anderen Fachbereichen der FHF. Mitgliedschaft im IHR Internet Hochschulradio. Im Wintersemester 2001/02 zum ersten Mal ausschließlich WebTV-live-Sendungen. Nur im automatisierten Betrieb bleibt das WebRadio erhalten.</p>
			</div>
			<div class="chronik-content chronik-content-left">
				<div class="bubble">
					<p class="number">2002</p>
				</div>
				<p class="text">Ab dem Sommersemester wird GLFtv rund um die Uhr zu hören bzw. sehen zu sein: von Montags bis Sonntags im automatisierten und dennoch interaktiven Betrieb sowie jeden Dienstag Abend von 19 bis 21 Uhr in der Live-Sendung</p>
			</div>
			<div class="chronik-content chronik-content-right">
				<div class="bubble">
					<p class="number">2003</p>
				</div>
				<p class="text">Ab dem Sommersemester 2003 Trennung von TV und Radio. Wurden die Web TV Sendungen bisher noch durch Musikblöcke unterbrochen, so wird nun in der ersten Stunde reines Web TV mit vorproduzierten Beiträgen sowie Live Interviews und Moderationen gezeigt. Die zweite Stunde ist der Musik vorbehalten. Es entsteht ein eigenständiges Webradio (mit fester Kameraeinstellung), das seit Januar 2004 den Namen GLFtv deLuxe trägt. Die Radiosendungen enthalten Moderationen mit Tipps zu Musik, Büchern, Veranstaltungen etc. sowie der Möglichkeit per Email oder Chat mit dem Moderator in Kontakt zu treten.</p>
			</div>
			<div class="chronik-content chronik-content-left">
				<div class="bubble">
					<p class="number">2005</p>
				</div>
				<p class="text">Mit Beginn des Wintersemesters 2004/05 wurde die bisherige Vorgabe, jeden Dienstag ein Sendefenster von zwei Stunden zu füllen, und somit auch die Radiosendung aufgegeben. Die Web TV Sendungen haben nun eine Länge von 45 bis 60 Minuten.</p>
			</div>
			<div class="chronik-content chronik-content-right">
				<div class="bubble">
					<p class="number">2007</p>
				</div>
				<p class="text">Im Sommersemester zieht GLFtv in ein neues, größeres Studio im Informatik Neubau (I-Bau).</p>
			</div>
			<div class="chronik-content chronik-content-left">
				<div class="bubble">
					<p class="number">2010</p>
				</div>
				<p class="text">GLF Campus TV sendet erstmals in HD-Qualität.</p>
			</div>
			<div class="chronik-content chronik-content-right">
				<div class="bubble">
					<p class="number">2014</p>
				</div>
				<p class="text">Im Februar 2014 launcht GLFtv die neue Website, mit der es möglich ist, alle Inhalte auch auf mobilen Endgeräten anzusehen. Zudem wird der GLFtv Blog integriert und mit der Themenseite eine Möglichkeit geschaffen, die Zuschauer zu integrieren und ihre Ideen in die Live-Sendungen zu bringen.</p>
